public class stringTimes {
    public String stringTimes(String str, int n) {
        String result = ""; // result is set to an empty string
        for (int i = 0; i < n; i++) { // sets i to 0, if n is greater than i, increment to n.
            result = result + str; // for each increment, the string is concatenated to result.
        } // this adds onto the string, which will end with a final "result" string.
    }

// given a string
// also given a non-negative integer
// return a larger string which is n copies of the original string
// Ex:(doubled the string) Hi -> HiHi

}
